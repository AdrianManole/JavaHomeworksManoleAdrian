    /**
     * Arithmetic operations which will be performed by the application
     * case 1 - addition (+), case 2 - subtraction (-), case 3 - mutiplication (*)
     * case 4 - division (/)
     */
    
    
    public void arithmeticOperations (){
        
        switch(calculation){
            
            case 1: // Addition
                answer = number + Double.parseDouble(calculatorScreen.getText());
                calculatorScreen.setText(Double.toString(answer));
                break;
                
            case 2: // Subtraction
                answer = number - Double.parseDouble(calculatorScreen.getText());
                calculatorScreen.setText(Double.toString(answer));
                break;
                
            case 3: // Multiplication
                answer = number * Double.parseDouble(calculatorScreen.getText());
                calculatorScreen.setText(Double.toString(answer));
                break;
                
            case 4: // Division
                answer = number / Double.parseDouble(calculatorScreen.getText());
                calculatorScreen.setText(Double.toString(answer));
                break;
                
        }
    }
    
