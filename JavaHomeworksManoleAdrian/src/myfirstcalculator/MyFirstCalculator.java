    /**
     * When the ← (back button) is clicked it erases one character at a time
     * from the calculator screen.
     */
    
    private void buttonBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBackActionPerformed
        int length = calculatorScreen.getText().length();
        int number = calculatorScreen.getText().length() - 1;
        String store;
        
        if (length > 0){
            StringBuilder back = new StringBuilder (calculatorScreen.getText());
            back.deleteCharAt(number);
            store = back.toString();
            calculatorScreen.setText(store);
        }
    }//GEN-LAST:event_buttonBackActionPerformed

